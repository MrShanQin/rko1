public class Homepageinfo {
    private String info;
    private String time;
    private int color_point;
    private int color;

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getColor_point() {
        return color_point;
    }

    public void setColor_point(int color_point) {
        this.color_point = color_point;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }
}
